    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        String bookerName = JOptionPane.showInputDialog("Enter Booker's Name", null);
        String opening = JOptionPane.showInputDialog("Enter Opening Cr Amount", null);
        double openingAmuount = Frmttr.getDoubleFromString(opening);

        if (bookerName != null) {
            Response insertResponse = Global.CONTROLLER.insertRecord(new Booker(bookerName, openingAmuount), "Booker");
            if (insertResponse.isSuccess()) {
                JOptionPane.showMessageDialog(this, "Record Added Successfully");
                addDataToTable();
            } else {
                JOptionPane.showMessageDialog(this, insertResponse.getErrorMsg());
            }
        }

    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if (jTable1.getSelectedRowCount() > 0 && jTable1.getSelectedRowCount() < 2) {
            int row = jTable1.getSelectedRow();
            AppUser user = (AppUser) jTable1.getValueAt(row, 0);
            Response updateRecord = Global.CONTROLLER.updateRecord(user.getUserId(), AppUser.class.getName());
            if (!updateRecord.isError()) {
                dtm.removeRow(row);
            } else {
                JOptionPane.showMessageDialog(this, updateRecord.getErrorMsg());
            }
        } else {
            JOptionPane.showMessageDialog(this, "Please select One record");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

