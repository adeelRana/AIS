    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        if (jTable1.getSelectedRowCount() > 0 && jTable1.getSelectedRowCount() < 2) {
            String cr = JOptionPane.showInputDialog("Enter Cr Amount", null);
            String remarks = JOptionPane.showInputDialog("remarks", null);

            if (remarks.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Remarks Cannot be empty ");
                remarks = JOptionPane.showInputDialog("remarks", null);
            }

            double crAmount = Frmttr.getDoubleFromString(cr);

            if (crAmount > 0) {
                Booker b = (Booker) jTable1.getValueAt(jTable1.getSelectedRow(), 0);

                BookerCr bkrCr = new BookerCr();
                bkrCr.setCrAmount(crAmount);
                bkrCr.setCrBooker(b);
                bkrCr.setCrDate(Global.DATE_UTIL.getNewDate().getTime());
                bkrCr.setCrInvoice(0);
                bkrCr.setCrPiad(0d);
                bkrCr.setCrRemarks(remarks);
                bkrCr.setCrStatus(Status.RECEIVEABLE.getValue());
                bkrCr.setCrType(CrType.HAND_CR.getValue());

                b.setCurrentCr(b.getCurrentCr() + crAmount);
                Response r = Global.CONTROLLER.insertRecord(bkrCr, "BookerCr");

                if (r.isSuccess()) {
                    if (Global.CONTROLLER.updateRecord(b, "Booker").isSuccess()) {
                        addDataToTable();
                    } else {
                        JOptionPane.showMessageDialog(this, "Error -1123");
                    }
                }
            }

        }
    }//GEN-LAST:event_jButton6ActionPerformed

