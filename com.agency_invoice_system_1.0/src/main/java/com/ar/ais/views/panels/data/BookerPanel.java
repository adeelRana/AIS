    private void addDataToTable() {
        dtm.setRowCount(0);
        ArrayList<Booker> list = (ArrayList<Booker>) Global.CONTROLLER.fetch("Booker").getResponseObject();
        for (Booker b : list) {
            dtm.addRow(new Object[]{
                b,
                b.getOpeningCr(),
                b.getCurrentCr(),
                Status.get(b.getStatus())
            });
        }
    }
