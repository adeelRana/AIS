    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if (jTable1.getSelectedRowCount() > 0 && jTable1.getSelectedRowCount() < 2) {
            int row = jTable1.getSelectedRow();
            Shop shop = (Shop) jTable1.getValueAt(row, 0);
            shop.setStatus(Status.IN_ACTIVE.getValue());
            Response updateRecord = Global.CONTROLLER.updateRecord(shop, "Shop");
            if (!updateRecord.isError()) {
                JOptionPane.showMessageDialog(this, "Rcord Updated");
                addDataToTable();
            } else {
                JOptionPane.showMessageDialog(this, updateRecord.getErrorMsg());
            }
        } else {
            JOptionPane.showMessageDialog(this, "Please select One record");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

