    private void addDataToTable() {
        dtm.setRowCount(0);
        ArrayList<Shop> list = (ArrayList<Shop>) Global.CONTROLLER.fetch("Shop").getResponseObject();

        for (Shop s : list) {
            dtm.addRow(new Object[]{
                s,
                s.getAddress(),
                s.getShopRoute(),
                s.getShopBooker(),
                s.getOpeningCr(),
                s.getCurrentCr(),
                s.showDiscount(),
                s.getStatus()
            });
        }
    }
